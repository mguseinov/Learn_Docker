/*
Конструкторы в Java — это специальные методы, 
которые используются для инициализации объектов.
Они автоматически вызываются при создании нового объекта и 
служат для задания начального состояния полей объекта.
*/

// Основные характеристики конструктора
// Имя конструктора совпадает с именем класса:

// Конструктор должен иметь то же имя, что и класс, чтобы компилятор понимал, что это именно конструктор.

class User {
    User() {
        System.out.println("Конструктор вызван");
    }
}

// Конструктор не имеет возвращаемого типа (даже void):

// Если указать возвращаемый тип, это будет считаться обычным методом, а не конструктором.
// Конструктор вызывается при создании объекта:

// Используется оператор new:
User user = new User(); // Конструктор вызван автоматически

// Конструкторы могут быть перегружены:
// Можно создавать несколько конструкторов с разным количеством и типами параметров.
// Если не объявить конструктор, компилятор создаст конструктор по умолчанию:

// Он будет пустым и не выполнять никакой работы, кроме создания объекта.

